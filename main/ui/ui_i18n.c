const char *_t(const char *es)
{
    if (!es || strcmp(ui_cfg_lang(), "en") != 0) return es;
    for (size_t i = 0; i < sizeof(TR) / sizeof(TR[0]); i++)
        if (strcmp(TR[i].es, es) == 0) return TR[i].en;
    return es; /* sin traducción: se muestra el español */
}
